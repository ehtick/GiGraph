namespace GiGraph.Dot.Output.Options;

/// <summary>
///     Text case options.
/// </summary>
public enum DotTextCase
{
    /// <summary>
    ///     Leaves the current text casing as is.
    /// </summary>
    Normal,

    /// <summary>
    ///     Converts text to upper case.
    /// </summary>
    Upper,

    /// <summary>
    ///     Converts text to lower case.
    /// </summary>
    Lower
}